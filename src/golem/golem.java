    private void configurarControlesTeclado() {
        JComponent root = (JComponent) this.getContentPane();
        root.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(KeyStroke.getKeyStroke(KeyEvent.VK_UP, 0), "moverArriba");
        root.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(KeyStroke.getKeyStroke(KeyEvent.VK_DOWN, 0), "moverAbajo");
        root.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(KeyStroke.getKeyStroke(KeyEvent.VK_LEFT, 0), "moverIzquierda");
        root.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(KeyStroke.getKeyStroke(KeyEvent.VK_RIGHT, 0), "moverDerecha");
        root.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(KeyStroke.getKeyStroke(KeyEvent.VK_W, 0), "moverArriba");
        root.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(KeyStroke.getKeyStroke(KeyEvent.VK_S, 0), "moverAbajo");
        root.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(KeyStroke.getKeyStroke(KeyEvent.VK_A, 0), "moverIzquierda");
        root.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(KeyStroke.getKeyStroke(KeyEvent.VK_D, 0), "moverDerecha");

        root.getActionMap().put("moverArriba", new AbstractAction() {
            @Override
            public void actionPerformed(ActionEvent e) {
                creaEscena.caminar();
                checkWin();
            }
        });

        root.getActionMap().put("moverAbajo", new AbstractAction() {
            @Override
            public void actionPerformed(ActionEvent e) {
                creaEscena.moverTorso(8, 0.0f, 0.0f, -0.05f);
                creaEscena.caminar();
                checkWin();
            }
        });

        root.getActionMap().put("moverIzquierda", new AbstractAction() {
            @Override
            public void actionPerformed(ActionEvent e) {
                creaEscena.moverTorso(8, -0.05f, 0.0f, 0.0f);
                creaEscena.caminar();
                checkWin();
            }
        });

        root.getActionMap().put("moverDerecha", new AbstractAction() {
            @Override
            public void actionPerformed(ActionEvent e) {
                creaEscena.moverTorso(8, 0.05f, 0.0f, 0.0f);
                creaEscena.caminar();
                checkWin();
            }
        });
    }

