    private void checkWin() {
        Vector3f pos = creaEscena.getPosicionTorso();
        if (pos.length() < 0.2) { // close to center
            victoriasJugador++;
            rondaActual++;
            lblPuntajes.setText("Puntajes: Tú " + victoriasJugador + " - Oponente " + victoriasOponente + " | Ronda " + rondaActual);
            lblEstado.setText("¡Ganaste la ronda!");
            if (rondaActual > 3) {
                juegoTerminado = true;
                mostrarFinJuego();
            } else {
                reiniciarRonda();
            }
        }
    }

    private void reiniciarRonda() {
        // Reset position
        creaEscena.moverTorso(8, 0.0f, 0.0f, 0.0f);
        lblEstado.setText("Ronda " + rondaActual + " comenzando...");
    }

    private void mostrarFinJuego() {
        lblEstado.setText("Juego terminado. Ganador: " + (victoriasJugador > victoriasOponente ? "Tú" : "Oponente"));
        // Add buttons for exit or lobby
        javax.swing.JButton btnSalir = new javax.swing.JButton("Salir");
        javax.swing.JButton btnLobby = new javax.swing.JButton("Regresar al Lobby");
        panelPuntajes.add(btnSalir);
        panelPuntajes.add(btnLobby);
        btnSalir.addActionListener(e -> System.exit(0));
        btnLobby.addActionListener(e -> {
            this.dispose();
            new LobbyFrame(usuarioLogueado, idUsuario).setVisible(true);
        });
        panelPuntajes.revalidate();
        panelPuntajes.repaint();
    }
